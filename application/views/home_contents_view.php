<h3>Com Lib ICS Library System</h3>

<p>
	The ICS Library System is designed to make the services offered by the ICS library more accessible and convenient by putting them available online. The system aims to automate the process of borrowing and lending of books from the library and as well as to manage and record the books and the borrowers. The users (students and faculty members) can search, view and borrow books given that they have logged in and the administrator (librarian) may update the status of the books to either available or borrowed, add/delete/disable/enable a user account.
</p>

