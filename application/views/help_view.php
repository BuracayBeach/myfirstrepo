<h3>Help</h3>

<p>
	<br>To <b>RESERVE</b> Reading Material
	<br>1. Search for books to check its status (borrowed, reserved, and available).
	<br>2. If book is available, hit “reserve” button located under the title of the book.
	<br>3. Confirm reservation by going to My Library.
	<br>4. You have one week to get the book you reserved in the library.
	<br>
	<br>To <b>CANCEL</b> a reservation
	<br>1. Go to My Library >> Reserved.
	<br>2. Look for the book you want to cancel your reservation.
	<br>3. You will find a button saying “unreserved”, click it.
	<br>
	<br>To <b>CREATE</b> an account:
	<br>1. Go to (url)
	<br>2. On the upper right corner of the screen, click the “Create Account” button.
	<br>3. Fill in all necessary information (name, password, etc.).
	<br>4. Submit application.
	<br>5. Wait for the approval of the administrator.
</p>

