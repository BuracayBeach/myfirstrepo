          <div class="tab-pane fade" id="history">
            <p>On January 26, 1995, the Institute of Computer Science was established by the UP Board of Regents. ICS is the system institute for computer sciences of the whole UP system.
On the same date, the ICS library, which was back then just a reading room, was also opened to cater the needs of Computer Science major.
It started keeping Thesis and Special Problem manuscripts. Little by little, books on related topics from around the world were added to the libraryâ€™s collection. As years pass, more books and publications were added. 
Adding, updating and keeping track of the books, borrowing and reservation processes, and other services offered were all done manually. It was simple in the beginning, but as the number of books increases, the harder it is to do all services. 
Finally on March 14, 2014, the ComLib: ICS Library System was finished to replace the tedious processes on services offered by the ICS Library. It is a web-based application where students create accounts to reserve and borrows books and made all library services easier to do. 
At present, the ICS library continues to add more materials to help more students not only in UPLB but also to those who has the interest and passion to the field.
</p>
